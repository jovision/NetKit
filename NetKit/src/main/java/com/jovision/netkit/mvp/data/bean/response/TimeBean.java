package com.jovision.netkit.mvp.data.bean.response;

public class TimeBean {
    /**
     * year : 2019
     * month : 9
     * day : 7
     * hour : 18
     * min : 47
     * sec : 10
     */

    private int year;
    private int month;
    private int day;
    private int hour;
    private int min;
    private int sec;

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getHour() {
        return hour;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getSec() {
        return sec;
    }

    public void setSec(int sec) {
        this.sec = sec;
    }
}
